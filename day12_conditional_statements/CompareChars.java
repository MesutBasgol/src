package day12_conditional_statements;

public class CompareChars {
    public static void main(String[] args) {
        char letter1 = 'A';
        char letter2 = 'B';
        System.out.println("A < B " + (letter1<letter2));
        System.out.println("A = B " + (letter1==letter2));
        System.out.println("A > B " + (letter1>letter2));
        System.out.println("A not equal B " + (letter1!=letter2));
    }
}
